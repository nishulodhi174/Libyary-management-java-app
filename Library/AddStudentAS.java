    public static void addStudent(String name, String password, String phone) {
        try {
            ConnectionClass obj1 = new ConnectionClass();
            
            // Create the SQL INSERT statement using PreparedStatement
            String s = "INSERT INTO student_data (student_name, password, phone) VALUES (?, ?, ?)";
            try (PreparedStatement preparedStatement = obj1.con.prepareStatement(s)) {
                preparedStatement.setString(1, name);
                preparedStatement.setString(2, password);
                preparedStatement.setString(3, phone);
                // Execute the query
//                ResultSet res = preparedStatement.executeQuery();
                int rowsAffected = preparedStatement.executeUpdate();


                
                if(rowsAffected > 0){
                    JOptionPane.showMessageDialog(null, "Student Created");
                    AdminSection af = new AdminSection();
                    af.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null, "Error Occured");
                }
            }
            obj1.con.close();
        } catch (SQLException e) {
            // Handle any exceptions that might occur during the database operation
        }
    }
